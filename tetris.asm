check_free:
# This function checks if a specific point on the game map is free (not occupied by another square).
# $a0: x, $a1: y

# First, it checks if the given coordinates are within the boundaries of the game area. 
# If the x-coordinate is less than 0 or greater than or equal to 10, 
# or if the y-coordinate is less than 0 or greater than or equal to 20, the point is considered "occupied" (the function returns 1).
blt	$a0, $0, check_ret_occ
bge	$a0, 10, check_ret_occ
blt	$a1, $0, check_ret_occ
bge	$a1, 20, check_ret_occ

# If the coordinates are within the game area, the function checks the value of this point on the game map with the load2d command.
# If the value of this point is not 0, it means that it is already occupied by another square, and the function also returns 1 to indicate a collision.
li	$t0, 10
la	$t1, GAME_MAP
load2d($t2, $a1, $a0, $t1, $t0)
# If the value of this point is 0, it means that it is idle and the function returns 0 to indicate that there is no collision.
bne	$t2, $0, check_ret_occ
move	$v0, $0
jr	$ra
check_ret_occ:
li	$v0, 1
jr	$ra
