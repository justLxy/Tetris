update_virtual_block:
push($ra)

lw	$t0, VIRTUAL_BLOCK
beq	$t0, 999, update_virtual_block_skip_clear_vblock
la	$a0, VIRTUAL_BLOCK
jal	clear_block
update_virtual_block_skip_clear_vblock:
# A series of lw (load word) and sw (store word) instructions copy the attributes of CURR_BLOCK to VIRTUAL_BLOCK.
# so that the initial state of the virtual cube is the same as the current one.
lw	$t0, CURR_BLOCK
sw	$t0, VIRTUAL_BLOCK
lw	$t0, CURR_BLOCK+4
sw	$t0, VIRTUAL_BLOCK+4
lw	$t0, CURR_BLOCK+8
sw	$t0, VIRTUAL_BLOCK+8

li	$t0, 1
update_vblock_while:
# Gradually increase the y-coordinate of the virtual square:
	lw	$t1, CURR_BLOCK+12
	add	$t1, $t1, $t0
	sw	$t1, VIRTUAL_BLOCK+12
# After changing the y-coordinate of the virtual cube, the block_coll_det function is called to check if a collision has occurred.
# If a collision occurs (i.e., block_coll_det returns a non-zero value), the loop ends.
	push($t0)
	la	$a0, VIRTUAL_BLOCK
	jal	block_coll_det
	pop($t0)
	bne	$v0, $0, update_vblock_while_end
	addi	$t0, $t0, 1
	j	update_vblock_while

update_vblock_while_end:
# At the end of the loop, the y-coordinate of VIRTUAL_BLOCK is decremented by 1 to reflect the last valid position before the collision occurred.
lw	$t1, VIRTUAL_BLOCK+12
addi	$t1, $t1, -1
sw	$t1, VIRTUAL_BLOCK+12
# If the y-coordinate is greater than 1, i.e. the virtual block is inside the interface, the draw_vblock function is called to draw the virtual block.
# This check prevents drawing a block when it should not be drawn (for example, when the block is completely outside the game area).
ble	$t1, 1, update_vblock_skip_draw_vblock

la	$a0, VIRTUAL_BLOCK
jal	draw_vblock
update_vblock_skip_draw_vblock:
# Finally, the pop instruction is used to restore the value of the $ra (return address) register, 
# and then the jr $ra instruction is used to return to the place where this function was called.
pop($ra)
jr	$ra
