# i vertical, j horizontal
.macro setpixel(%i, %j, %val)
	lw	$t9, ADDR_DSPL
	mul	$t8, %i, SCREEN_W
	add	$t8, $t8, %j
	sll	$t8, $t8, 2    # (i * w + j) * 4
	add	$t8, $t8, $t9  # ADDR_DSPL[i][j]
	sw	%val, ($t8) # show color in ADDR_DSPL[i][j]
.end_macro

# reflection(It's useless)
.macro setpixelneg(%i, %j, %val)
	lw	$t9, ADDR_DSPL
	mul	$t8, %i, SCREEN_W
	add	$t8, $t8, %j
	sll	$t8, $t8, 2
	add	$t8, $t8, $t9
	xori	$t9, %val, 0xffffff
	sw	$t9, ($t8)
.end_macro

# This macro is used to load the value of an element from a one-dimensional array.
.macro load1d(%dst, %i, %base)
	sll	$t9, %i, 2
	add	$t9, $t9, %base
	lw	%dst, ($t9)    # dst = base[i]
.end_macro

# This macro is used to store the value of an element to a specified location in a one-dimensional array.
.macro store1d(%src, %i, %base)
	sll	$t9, %i, 2
	add	$t9, $t9, %base
	sw	%src, ($t9) # base[i] = src
.end_macro

# This macro is used to load the value of an element from a two-dimensional array.
.macro load2d(%dst, %i, %j, %base, %len)
	mul	$t9, %i, %len
	add	$t9, $t9, %j
	sll	$t9, $t9, 2
	add	$t9, $t9, %base
	lw	%dst, ($t9) # dst = base[i][j]
.end_macro

# This macro is used to store the value of an element to a specified location in a two-dimensional array.
.macro store2d(%src, %i, %j, %base, %len)
	mul	$t9, %i, %len
	add	$t9, $t9, %j
	sll	$t9, $t9, 2
	add	$t9, $t9, %base
	sw	%src, ($t9) # base[i][j] = src
.end_macro

.macro push(%r)
	addi	$sp, $sp, -4
	sw	%r, 0($sp)
.end_macro

.macro pop(%r)
	lw	%r, 0($sp)
	addi	$sp, $sp, 4
.end_macro

# This macro is used to get the memory address of the shape data corresponding to the shape and rotation state of the specified square.
.macro get_block_shape_ptr(%dst, %block_ptr)
	lw	$t9, (%block_ptr)	# shape
	lw	$t7, 4(%block_ptr)	# dir
	sll	$t9, $t9, 2
	la	$t8, BLOCK_SHAPE_LIST
	add	$t9, $t8, $t9
	lw	$t9, ($t9)	# get BLOCK_SHAPE_i
	addi	$t9, $t9, 4	# skip color
	sll	$t7, $t7, 6    # *64
	add	%dst, $t9, $t7
.end_macro

# This macro is used to get the color information corresponding to the specified square shape.
.macro get_block_color(%dst, %block_ptr)
	lw	$t9, (%block_ptr)	# shape
	sll	$t9, $t9, 2
	la	$t8, BLOCK_SHAPE_LIST
	add	$t9, $t8, $t9
	lw	$t9, ($t9)	# get BLOCK_SHAPE_i
	lw	%dst, ($t9)
.end_macro

