coll_det:
# This function is used to detect if a cube collides with another cube on the game map.
# $a0: x, $a1: y
# $a2: shape_addr

# The function iterates through each cell of the cube shape (a 4x4 matrix), and for each non-zero element of the matrix, 
# it computes the actual coordinates of the cube on the game map and calls check_free to check if this position is free.
# If check_free returns 1 (indicating a collision), coll_det also returns 1.
# If the positions of all non-zero elements are free, the function returns 0, indicating no collision.
push($ra)
li	$t0, 0
coll_det_loop1:
	beq	$t0, 4, coll_det_loop_end1
	li	$t1, 0
	coll_det_loop2:
	# If the value of the current element is 0, it means that the position is blank in the shape matrix and there is no need to detect a collision, 
	# so it is skipped and the next iteration of the inner loop is continued.
		beq	$t1, 4, coll_det_loop_end2
		li	$t2, 4
		load2d($t3, $t0, $t1, $a2, $t2)
		beq	$t3, $0, coll_det_skip
		push($a0)
		push($a1)
		push($a2)
		push($t0)
		push($t1)
		add	$a0, $a0, $t1
		add	$a1, $a1, $t0
		jal	check_free
		pop($t1)
		pop($t0)
		pop($a2)
		pop($a1)
		pop($a0)
		bne	$v0, $0, coll_det_ret_coll

		coll_det_skip:
		addi	$t1, $t1, 1
		j	coll_det_loop2
	coll_det_loop_end2:
	addi	$t0, $t0, 1
	j	coll_det_loop1
coll_det_loop_end1:

li	$v0, 0
j	coll_det_before_ret

coll_det_ret_coll:
li	$v0, 1

coll_det_before_ret:
pop($ra)
jr	$ra
